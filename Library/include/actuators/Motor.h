#pragma once

#include "actuators/JointActuator.h"

namespace sf
{
    //! A class representing a motor.
    class Motor : public JointActuator
    {
    public:
        //! A constructor.
        /*!
         \param uniqueName a name for the motor
         */
        Motor(const std::string& uniqueName);
        
        //! A destructor.
        virtual ~Motor() = default;

        //! A method used to update the internal state of the actuator.
        /*!
         \param dt the time step of the simulation [s]
         */
        void Update(Scalar dt);

        //! A method to set the motor torque.
        /*!
         \param tau a value of the motor torque [Nm]
         */
        void setTorque(Scalar tau);

        //! A method used to set the torque limit.
        /*!
         \param tau value of the torque limit
        */
        void setTorqueLimit(Scalar tau);
                
        //! A method returning the torque generated by the motor.
        Scalar getTorque() const;
        
        //! A method returning the angular position of the motor.
        Scalar getAngle() const;
        
        //! A method returning the angular velocity of the motor.
        Scalar getAngularVelocity() const;
        
        //! A method returning type of joint actuator.
        JointActuatorType getJointActuatorType() const override;

        //! A method returning the construction info for the actuator.
        static ConstructInfo getConstructInfo();

        //! A method constructing the actuator based on info structure.
        /*!
         \param info a construction info structure
        */
        static std::unique_ptr<Motor> Construct(const std::string& uniqueName, ConstructInfo& info);
        
    private:
        void WatchdogTimeout() override;

        Scalar torque_;
        Scalar limit_;
    };
}
